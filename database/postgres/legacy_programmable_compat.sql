CREATE OR REPLACE FUNCTION dbo."sp_creatediagram"(
  p_diagramname varchar(128),
  p_owner_id integer DEFAULT NULL,
  p_version integer DEFAULT NULL,
  p_definition bytea DEFAULT NULL
)
RETURNS integer
LANGUAGE plpgsql
AS $$
DECLARE
  v_owner_id integer := coalesce(p_owner_id, 1);
  v_retval integer;
BEGIN
  IF p_version IS NULL OR p_diagramname IS NULL THEN
    RETURN -1;
  END IF;

  IF EXISTS (
    SELECT 1
    FROM dbo."sysdiagrams"
    WHERE "principal_id" = v_owner_id
      AND "name" = p_diagramname
  ) THEN
    RETURN -2;
  END IF;

  INSERT INTO dbo."sysdiagrams" ("name", "principal_id", "version", "definition")
  VALUES (p_diagramname, v_owner_id, p_version, p_definition)
  RETURNING "diagram_id" INTO v_retval;

  RETURN v_retval;
END;
$$;

CREATE OR REPLACE FUNCTION dbo."sp_alterdiagram"(
  p_diagramname varchar(128),
  p_owner_id integer DEFAULT NULL,
  p_version integer DEFAULT NULL,
  p_definition bytea DEFAULT NULL
)
RETURNS integer
LANGUAGE plpgsql
AS $$
DECLARE
  v_owner_id integer := coalesce(p_owner_id, 1);
BEGIN
  IF p_diagramname IS NULL THEN
    RETURN -1;
  END IF;

  IF NOT EXISTS (
    SELECT 1
    FROM dbo."sysdiagrams"
    WHERE "principal_id" = v_owner_id
      AND "name" = p_diagramname
  ) THEN
    RETURN -3;
  END IF;

  UPDATE dbo."sysdiagrams"
  SET "definition" = coalesce(p_definition, "definition"),
      "version" = coalesce(p_version, "version")
  WHERE "principal_id" = v_owner_id
    AND "name" = p_diagramname;

  RETURN 0;
END;
$$;

