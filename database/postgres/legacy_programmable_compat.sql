CREATE OR REPLACE FUNCTION dbo."ExtraerEnteros"(p_string varchar)
RETURNS varchar(1000)
LANGUAGE sql
IMMUTABLE
AS $$
  SELECT left(regexp_replace(coalesce(p_string, ''), '[^0-9]', '', 'g'), 1000)::varchar(1000);
$$;

